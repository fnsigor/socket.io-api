import { DataTypes, Model } from "sequelize";
import { sequelize } from "../config/database";

export class Pizza extends Model {}
Pizza.init(
  {
    id: {
      type: DataTypes.INTEGER,
      primaryKey: true,
      autoIncrement: true,
    },
    name: {
      type: DataTypes.STRING,
      allowNull: false,
    },
    price: {
      type: DataTypes.FLOAT,
      allowNull: false,
    },
  },
  {
    sequelize,
    modelName: "Pizza",
  }
);
